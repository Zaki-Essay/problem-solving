package problemSolving;

public class MultiplicationTable {


    public static void main(String args[]){



        int number1=1;
        int number2=10;
        int number3=11;
        int number4=20;
        int number5=21;
        int number6=30;
        for (int i = 1; i <=10 ; i++) {



                System.out.println(number1+"\t"+number2+"\t"+number3+"\t"+number4+"\t"+number5+"\t"+number6+"\n");
             number1++;
             number2--;
             number3++;
             number4--;
             number5++;
             number6--;



        }




    }
}
